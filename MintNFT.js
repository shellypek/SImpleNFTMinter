const contractAddress = "0xa50D76314357A164b659cb568E1D970429aac55e";

export const MintNFT = () => {
  const web3 = useWeb3();
  const [contract, setContract] = useState(null);
  const [account, setAccount] = useState(null);

  useEffect(() => {
    if (web3) {
      const instance = new Contract(contractABI, contractAddress);
      setContract(instance);
      web3.eth.getAccounts().then(accounts => setAccount(accounts[0]));
    }
  }, [web3]);

  const mint = async () => {
    if (!contract || !account) {
      console.error("Contract or account not found");
      return;
    }

    try {
      const result = await contract.methods.safeMint(account).send({ from: account, value: web3.utils.toWei("0.001", "ether") });
      console.log("Minted NFT with tokenId:", result.events.Transfer.returnValues.tokenId);
    } catch (error) {
      console.error("Failed to mint NFT:", error);
    }
  };

  return <button onClick={mint}>Mint NFT</button>;
};